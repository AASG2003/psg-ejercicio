# RemoveSuffix
pal = "Hola Mundo Como estan?"
remPal = pal.removesuffix('Como estan?')
print(remPal)
# RemovePreffix
pal = "Hola Mundo Como estan?"
remPal = pal.removeprefix('Hola Mundo ')
print(remPal)
# Partition
pal = "Hola Mundo Como estan?"
remPal = pal.partition('Mundo')
print(remPal)
#IsUpper
pal = "BANANA"
up = pal.isupper()
print(up)
#IsTitle
pal = "Banana"
up = pal.istitle()
print(up)